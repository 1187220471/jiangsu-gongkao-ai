function getRandomTopics(type: string, count: number): string[] {
  const topics = QUESTION_TOPICS[type] || QUESTION_TOPICS['social']
  const shuffled = [...topics].sort(() => Math.random() - 0.5)
  return shuffled.slice(0, count)
}

